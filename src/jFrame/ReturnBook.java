    public void getIssueBookDetails() {

        int bookId = Integer.parseInt(txt_Bookid1.getText());
        int studentId = Integer.parseInt(txt_Studentid.getText());

        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `issue_books`"
                    + "INNER JOIN `book_details` ON `book_details`.`book_id` = `issue_books`.`book_details_book_id`"
                    + "INNER JOIN `student_details` ON `student_details`.`student_id` = `issue_books`.`student_details_student_id`"
                    + " WHERE `book_details_book_id` ='" + bookId + "' AND `student_details_student_id` ='" + studentId + "' ");

            if (resultSet.next()) {
                jLabel8issueid.setText(resultSet.getString("issue_books.issue_id"));
                jLabel10bookname.setText(resultSet.getString("book_details.book_name"));
                jLabel11studentname.setText(resultSet.getString("student_details.student_name"));
                jLabel12issuedate.setText(resultSet.getString("issue_books.issue_date"));
                jLabel7duedate.setText(resultSet.getString("issue_books.due_date"));

            } else {
                jLabel12bookerror.setText(" No Book Found ");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

