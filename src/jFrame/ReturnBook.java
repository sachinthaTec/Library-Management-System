    private void rSMaterialButtonCircle4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle4ActionPerformed
        // TODO add your handling code here:
        String book_id = txt_Bookid1.getText();
        String student_id = txt_Studentid.getText();
        

//        Date issue_date = jLabel12issuedate.getDate();
//        Date due_date = duedate.getDate();
        if (book_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter book id", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (student_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter student id", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `issue_books`WHERE `issue_id`='" + student_id + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, " Book Not Submited", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

//                    Date date = new Date();
//                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    MySQL2.executeIUD("UPDATE `issue_books` SET `book_status_book_status_id` = '2' WHERE `book_details_book_id` = '" + book_id + "' AND `student_details_student_id` = '" + student_id + "'");
                    
                    
                    updateBookCount();

//                    loadBookDetails();
                    JOptionPane.showMessageDialog(this, "Book Return successfully", "Warning", JOptionPane.WARNING_MESSAGE);
                    reset();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_rSMaterialButtonCircle4ActionPerformed

